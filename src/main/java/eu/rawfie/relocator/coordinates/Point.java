package eu.rawfie.relocator.coordinates;

public class Point {

	public double lat;
	public double log;
	public double height;
	public double x;
	public double y;
	public double z;

    public Point(double d1,double d2,double d3,double d4,double d5,double d6)
    {
    	this.lat = d1;
    	this.log = d2;
    	this.height = d3;
    	this.x = d4;
    	this.y = d5;
    	this.z = d6;
    }

}
